    public Path findPath(Road beginingRoad, Road finalRoad, Path lastPath) 
    {
        lastPath.changePath(beginingRoad);
        if(beginingRoad.equals(finalRoad))return lastPath;
        else 
        {
            Path leftPath = null;
            Path rightPath = null;
            Path straightPath = null;

            if(beginingRoad.leftTrue() && !lastPath.roadMark(beginingRoad.nextRoadLeft)) 
            {
                leftPath = lastPath.getNewPath();
                leftPath = findPath(beginingRoad.nextRoadLeft, finalRoad, leftPath);
            }
            if(beginingRoad.rightTrue() && !lastPath.roadMark(beginingRoad.nextRoadRight)) 
            {
                rightPath = lastPath.getNewPath();
                rightPath = findPath(beginingRoad.nextRoadRight, finalRoad, rightPath);
            }
            if(beginingRoad.straightTrue() && !lastPath.roadMark(beginingRoad.nextRoadStraight))
            {
            	straightPath = lastPath.getNewPath();
            	straightPath = findPath(beginingRoad.nextRoadStraight, finalRoad, straightPath);
            }
            Path best = null;
            if(leftPath != null) best = leftPath;
            if(best == null && rightPath != null) best = rightPath;
            else if(rightPath != null) 
            {
                if(rightPath.getPathTime() < best.getPathTime())best = rightPath;
            }
            if(best == null && straightPath != null)best = straightPath;
            else if(straightPath != null) 
            {
                if(straightPath.getPathTime() < best.getPathTime())best = straightPath;
            }
            return best;
        }
    }
